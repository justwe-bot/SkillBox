const translations: Record<AppLanguage, TranslationDictionary> = {
  'zh-CN': zhCN,
  'en-US': enUS,
}

export function getSystemLocales() {
  if (typeof window === 'undefined') {
    return ['zh-CN']
  }

  const locales = Array.isArray(window.navigator.languages) && window.navigator.languages.length
    ? window.navigator.languages
    : [window.navigator.language]

  return locales.filter(Boolean)
}

export function resolveInitialLanguage(systemLocales: readonly string[] = getSystemLocales()): AppLanguage {
  for (const locale of systemLocales) {
    const normalized = locale.toLowerCase()
    if (normalized.startsWith('zh')) {
      return 'zh-CN'
    }

    if (normalized.startsWith('en')) {
      return 'en-US'
    }
  }

  return 'zh-CN'
}

function interpolate(template: string, params?: TranslationParams) {
  if (!params) {
    return template
  }

  return template.replace(/\{(\w+)\}/g, (_, key: string) => {
    const value = params[key]
    return value === undefined || value === null ? '' : String(value)
  })
}

export function getTranslator(language: AppLanguage) {
  const dictionary = translations[language]

  return (key: TranslationKey, params?: TranslationParams) => {
    return interpolate(dictionary[key], params)
  }
}

export function formatDate(value: string | null, language: AppLanguage) {
  if (!value) {
    return getTranslator(language)('common.notChecked')
  }

  const date = new Date(value)
  if (Number.isNaN(date.getTime())) {
    return value
  }

  return new Intl.DateTimeFormat(language, {
    year: 'numeric',
    month: '2-digit',
    day: '2-digit',
    hour: '2-digit',
    minute: '2-digit',
  }).format(date)
}

export function localizeSkillSource(source: string, language: AppLanguage) {
  if (source === LOCAL_SYNC_SOURCE) {
    return getTranslator(language)('common.localSyncDirectory')
  }

  return source
}

export function localizeBackendSuccessMessage(message: string, language: AppLanguage) {
  switch (message) {
    case '已从远程仓库拉取最新内容':
      return language === 'en-US' ? 'Pulled the latest content from the remote repository' : message
    case '已完成拉取、同步并推送到远程仓库':
      return language === 'en-US' ? 'Pulled, synced, and pushed to the remote repository' : message
    case '已将本地 skills 与远程仓库合并':
      return language === 'en-US' ? 'Merged local skills with the remote repository' : message
    default:
      return message
  }
}
